-- DML para crear la tabla CLAP
CREATE TABLE CLAP (
    INICIO6_TARJETA VARCHAR(6),
    FINAL4_TARJETA VARCHAR(4),
    TIPO_TRX VARCHAR(20),
    MONTO DECIMAL(18, 2),
    FECHA_TRANSACCION DATE,
    CODIGO_AUTORIZACION VARCHAR(20),
    ID_BANCO VARCHAR(20),
    FECHA_RECEPCION_BANCO DATE
);

-- DML para crear la tabla BANSUR
CREATE TABLE BANSUR (
    TARJETA VARCHAR(16),
    TIPO_TRX VARCHAR(20),
    MONTO DECIMAL(18, 2),
    FECHA_TRANSACCION DATE,
    CODIGO_AUTORIZACION VARCHAR(20),
    ID_ADQUIRENTE VARCHAR(20),
    FECHA_RECEPCION DATE
);
